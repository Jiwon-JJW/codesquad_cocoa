abstract class moveUnit {
    protected int Characterx;
    protected int Charactery;
    protected int Monsterx;
    protected int Monstery;
    protected int Bombx;
    protected int Bomby;


    public int getCharacterx() {
        return Characterx;
    }

    public int getCharactery() {
        return Charactery;
    }

    public int getMonsterx() {
        return Monsterx;
    }

    public int getMonstery() {
        return Monstery;
    }

    public int getBomby() {
        return Bomby;
    }

    public int getBombx() {
        return Bombx;
    }

    public void setCharacterx(int characterx) {
        this.Characterx = characterx;
    }

    public void setCharactery(int charactery) {
        this.Charactery = charactery;
    }

    public void setMonsterx(int monsterx) {
        this.Monsterx = monsterx;
    }

    public void setMonstery(int monstery) {
        this.Monstery = monstery;
    }

    public void setBomby(int bomby) {
        this.Bomby = bomby;
    }

    public void setBombx(int bombx) {
        this.Bombx = bombx;
    }

}