abstract class Unit {
    protected int x;
    protected int y;
    protected int stage;


    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getStage() {
        return stage;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setStage(int stage) {
        this.stage = stage;
    }
}